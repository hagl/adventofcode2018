sourceR :: Integer -> Integer -> State -> (Integer, Integer)
sourceR 0 0 (State r0 r1 r2 r3) = (r0, r0)
sourceR 0 1 (State r0 r1 r2 r3) = (r0, r1)
sourceR 0 2 (State r0 r1 r2 r3) = (r0, r2)
sourceR 0 3 (State r0 r1 r2 r3) = (r0, r3)
sourceR 1 0 (State r0 r1 r2 r3) = (r1, r0)
sourceR 1 1 (State r0 r1 r2 r3) = (r1, r1)
sourceR 1 2 (State r0 r1 r2 r3) = (r1, r2)
sourceR 1 3 (State r0 r1 r2 r3) = (r1, r3)
sourceR 2 0 (State r0 r1 r2 r3) = (r2, r0)
