sourceR 3 2 (State r0 r1 r2 r3) = (r3, r2)
sourceR 3 3 (State r0 r1 r2 r3) = (r3, r3)

sourceI :: Integer -> Integer -> State -> (Integer, Integer)
sourceI 0 b (State r0 r1 r2 r3) = (r0, b)
sourceI 1 b (State r0 r1 r2 r3) = (r1, b)
sourceI 2 b (State r0 r1 r2 r3) = (r2, b)
sourceI 3 b (State r0 r1 r2 r3) = (r3, b)

dest :: Integer -> Integer -> State -> State
dest 0 v (State r0 r1 r2 r3) = (State v r1 r2 r3)
dest 1 v (State r0 r1 r2 r3) = (State r0 v r2 r3)
dest 2 v (State r0 r1 r2 r3) = (State r0 r1 v r3)
dest 3 v (State r0 r1 r2 r3) = (State r0 r1 r2 v)

swap (a, b) = (b, a)

combineR op a b c s = dest c ((uncurry op) (sourceR a b s)) s
combineI op a b c s = dest c ((uncurry op) (sourceI a b s)) s
combineIR op a b c s = dest c ((uncurry op) . swap $ (sourceI b a s)) s

